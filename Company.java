public class Company {
    
    int companyID;
    String companyName;
    int noAssociatedStock;
    
    public Company(String name) {
        name = companyName;
    }
    
    public String getName() {
        return companyName;
    }
    
    public int getStockQuantity() {
        return noAssociatedStock;
    }
    
    public int getCompanyID() {
        return companyID;
    }
    
}
